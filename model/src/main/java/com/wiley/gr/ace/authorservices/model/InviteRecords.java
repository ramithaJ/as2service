package com.wiley.gr.ace.authorservices.model;

/**
 * The Class InviteRecords.
 */
public class InviteRecords {

    /** The guid. */
    private String guid;

    /** The first name. */
    private String firstName;

    /** The last name. */
    private String lastName;

    /** The email address. */
    private String emailAddress;

    /** The user type. */
    private String userType;

    /** The status. */
    private String status;

    /**
     * @return the guid
     */
    public final String getGuid() {
        return guid;
    }

    /**
     * @param guid
     *            the guid to set
     */
    public final void setGuid(final String guid) {
        this.guid = guid;
    }

    /**
     * @return the firstName
     */
    public final String getFirstName() {
        return firstName;
    }

    /**
     * @param firstName
     *            the firstName to set
     */
    public final void setFirstName(final String firstName) {
        this.firstName = firstName;
    }

    /**
     * @return the lastName
     */
    public final String getLastName() {
        return lastName;
    }

    /**
     * @param lastName
     *            the lastName to set
     */
    public final void setLastName(final String lastName) {
        this.lastName = lastName;
    }

    /**
     * @return the emailAddress
     */
    public final String getEmailAddress() {
        return emailAddress;
    }

    /**
     * @param emailAddress
     *            the emailAddress to set
     */
    public final void setEmailAddress(final String emailAddress) {
        this.emailAddress = emailAddress;
    }

    /**
     * @return the userType
     */
    public final String getUserType() {
        return userType;
    }

    /**
     * @param userType
     *            the userType to set
     */
    public final void setUserType(final String userType) {
        this.userType = userType;
    }

    /**
     * @return the status
     */
    public final String getStatus() {
        return status;
    }

    /**
     * @param status
     *            the status to set
     */
    public final void setStatus(final String status) {
        this.status = status;
    }

}
